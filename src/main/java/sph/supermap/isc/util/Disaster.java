package sph.supermap.isc.util;

public class Disaster {
	private String disaster_type;
	private String type_order;
	private String search_date;
	
	public Disaster() {
		
	}
	
	public String getDisaster_type() {
		return disaster_type;
	}
	public void setDisaster_type(String disaster_type) {
		this.disaster_type = disaster_type;
	}
	public String getType_order() {
		return type_order;
	}
	public void setType_order(String type_order) {
		this.type_order = type_order;
	}
	public String getSearch_date() {
		return search_date;
	}
	public void setSearch_date(String search_date) {
		this.search_date = search_date;
	}
}
